        public static string IntToHexColour(int index)
        {
            return AvailableColors[index % AvailableColors.Count];
        }

        internal static string ExtensionToHex(string extension)
        {
            // find index of extension in list
            var index = FileExtensions.IndexOf(extension);
            if (index == -1)
            {
                return "#cfcfcf";
            }

            return IntToHexColour(index);
        }
